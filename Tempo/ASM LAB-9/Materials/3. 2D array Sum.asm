.MODEL SMALL
.STACK 100H
.DATA
	A DW 1,0,0,1 
	  DW 1,0,0,0
	  DW 1,3,1,0 
	  
	ROW DB 0
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	 
	MOV AX,0	;CONTAINS SUM
	MOV BX,0	;CONTAINS ADDRESS OF Ith ROW
	MOV SI,0	;CONTAINS Jth ELEMENT IN Ith ROW
	 
UP:          
	CMP ROW,2
	JG EXIT
	MOV CX,4 	;EACH ROW CONTAINS 4 ELEMENTS

ROW_SUM:   
	ADD AX,A[BX+SI]
	ADD SI,2 
	LOOP ROW_SUM  

	ADD BX,8 	; N*S = 4*2 = 8
	MOV SI,0
	INC ROW
	JMP UP

EXIT:
	;SUM IS IN AX
	

	MOV AH,4CH
    INT 21H         
    MAIN ENDP
END MAIN
